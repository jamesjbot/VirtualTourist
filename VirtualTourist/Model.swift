import UIKit

class Model: NSObject {
    
    // Mark: - Singleton
    private override init(){}
    
    class func sharedInstance() -> Model {
        struct Singleton {
            static var sharedInstance = Model()
        }
        return Singleton.sharedInstance
    }
    
}
